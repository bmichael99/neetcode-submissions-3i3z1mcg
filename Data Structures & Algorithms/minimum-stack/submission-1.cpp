//have the stack keep track of its local min, so stack is a stack of pairs where each element in the stack keeps track of the min in relationship to itself and the numbers below it in the stack. That way the top of stack always shows the min value.
class MinStack {
public:
    stack<pair<int,int>> st;
    MinStack() {
        
    }
    
    void push(int val) {
        if(st.empty()){
          st.push({val,val});
        } else {
          st.push({val,min(val,st.top().second)});
        }
    }
    
    void pop() {
        st.pop();
    }
    
    int top() {
        return st.top().first;
    }
    
    int getMin() {
        return st.top().second;
    }
};

